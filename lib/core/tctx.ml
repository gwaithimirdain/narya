(* To first approximation, type-level contexts form the free category generated by (1) modality generators, for locks and (2) variables, at a given mode (which is both the "source" and "target"), with a given dimension, and annotated by a given modality.  The "mode of the context" is its *source* as a morphism in this free category.

   Its target, in this free category, would be the mode of the empty context from which it was built.  But we are never interested in that, so it would be annoying to have to track it all the time.  Therefore, we add an extra dummy object and generating morphism to it from every mode, so that every context can have the same target. *)

type ('x, 'a) dim_entry = private Dim_entry_dummy
type 'a lock_entry = private Lock_entry_dummy
type 'a proj = private Proj_dummy

module ModeUnit = struct
  type _ t = Mode : 'a Mode.t -> 'a t | Unit : unit t

  let compare : type a b. a t -> b t -> (a, b) Eq.compare =
   fun a b ->
    match (a, b) with
    | Mode a, Mode b -> Mode.compare a b
    | Unit, Unit -> Eq
    | _ -> Neq
end

module TEntry = struct
  module Obj = ModeUnit

  type (_, _, _) t =
    | Dim :
        ('dom, 'modality, 'mode) Modality.t * 'a D.t
        -> ('mode, ('modality, 'a) dim_entry, 'mode) t
    | Lock : ('dom, 'mu, 'cod) Modality.gen -> ('dom, 'mu lock_entry, 'cod) t
    | Proj : 'mode Mode.t -> ('mode, 'mode proj, unit) t

  let src : type src m tgt. (src, m, tgt) t -> src Obj.t = function
    | Dim (x, _) -> Mode (Modality.tgt x)
    | Lock m -> Mode (Modality.Gen.src m)
    | Proj m -> Mode m

  let tgt : type src m tgt. (src, m, tgt) t -> tgt Obj.t = function
    | Dim (x, _) -> Mode (Modality.tgt x)
    | Lock m -> Mode (Modality.Gen.tgt m)
    | Proj _ -> Unit

  let src_uniq : type src1 m tgt1 src2 tgt2.
      (src1, m, tgt1) t -> (src2, m, tgt2) t -> (src1, src2) Eq.t =
   fun m1 m2 ->
    match (m1, m2) with
    | Dim (m1, _), Dim (m2, _) -> Modality.tgt_uniq m1 m2
    | Lock m1, Lock m2 -> Modality.Gen.src_uniq m1 m2
    | Proj _, Proj _ -> Eq

  let tgt_uniq : type src1 m tgt1 src2 tgt2.
      (src1, m, tgt1) t -> (src2, m, tgt2) t -> (tgt1, tgt2) Eq.t =
   fun m1 m2 ->
    match (m1, m2) with
    | Dim (m1, _), Dim (m2, _) -> Modality.tgt_uniq m1 m2
    | Lock m1, Lock m2 -> Modality.Gen.tgt_uniq m1 m2
    | Proj _, Proj _ -> Eq

  let compare : type d1 c1 d2 c2 g1 g2.
      (d1, g1, c1) t -> (d2, g2, c2) t -> (d1 * g1 * c1, d2 * g2 * c2) Eq.compare =
   fun x y ->
    match (x, y) with
    | Dim (x, a), Dim (y, b) -> (
        match (Modality.compare x y, D.compare a b) with
        | Eq, Eq -> Eq
        | _ -> Neq)
    | Lock x, Lock y -> (
        match Modality.Gen.compare x y with
        | Eq -> Eq
        | Neq -> Neq)
    | Proj m, Proj n -> (
        match Mode.compare m n with
        | Eq -> Eq
        | Neq -> Neq)
    | _ -> Neq
end

module Tctxcat = Path.Make (TEntry)

type 'mode emp = (unit Tctxcat.id, 'mode proj) Tctxcat.suc
type 'mode id = 'mode Tctxcat.id
type ('x, 'y) suc = ('x, 'y) Tctxcat.suc

module Tctx = struct
  include Tctxcat

  type nonrec ('mode, 'a) t = ('mode, 'a, unit) t

  let emp : type mode. mode Mode.t -> (mode, mode emp) t =
   fun mode -> Path (Suc (Zero, Proj mode), Unit)
end
(* We can compose all the locks in a tctx to produce a modality.  This *doesn't* work on the extra object, though, so we raise an error if it's applied to that.  So this isn't used on a whole context, only on context extensions.  *)

module Locksmap = struct
  module Dom = TEntry
  module Cod = Modality

  module Obj = struct
    module Dom = ModeUnit
    module Cod = Mode

    type (_, _) t = Eq : 'a Mode.t -> ('a, 'a) t

    let dom : type a x. (a, x) t -> a Dom.t = fun (Eq a) -> Mode a
    let cod : type a x. (a, x) t -> x Cod.t = fun (Eq a) -> a

    type _ exists = Exists : ('a, 'x) t -> 'a exists

    let exists : type a. a Dom.t -> a exists = function
      | Mode a -> Exists (Eq a)
      | Unit -> raise (Failure "IdModeUnit.exists on Unit")

    let uniq : type a x1 x2. (a, x1) t -> (a, x2) t -> (x1, x2) Eq.t =
     fun x1 x2 ->
      match (x1, x2) with
      | Eq _, Eq _ -> Eq
  end

  type (_, _, _, _, _, _) t =
    | Locks_dim :
        ('dom, 'annote, 'mode) Modality.t * 'a D.t
        -> ('mode, ('annote, 'a) dim_entry, 'mode, 'mode, 'mode Modality.id, 'mode) t
    | Locks_lock :
        ('x, 'm, 'y) Modality.gen
        -> ('x, 'm lock_entry, 'y, 'x, ('y Modality.id, 'm) snoc, 'y) t

  let dom : type a m b x n y. (a, m, b, x, n, y) t -> (a, m, b) Dom.t = function
    | Locks_dim (x, a) -> Dim (x, a)
    | Locks_lock m -> Lock m

  let cod : type a m b x n y. (a, m, b, x, n, y) t -> (x, n, y) Cod.t = function
    | Locks_dim (x, _) -> Modality.id (Modality.tgt x)
    | Locks_lock m -> Modality.of_gen m

  let src : type a m b x n y. (a, m, b, x, n, y) t -> (a, x) Obj.t = function
    | Locks_dim (x, _) -> Eq (Modality.tgt x)
    | Locks_lock m -> Eq (Modality.Gen.src m)

  let tgt : type a m b x n y. (a, m, b, x, n, y) t -> (b, y) Obj.t = function
    | Locks_dim (x, _) -> Eq (Modality.tgt x)
    | Locks_lock m -> Eq (Modality.Gen.tgt m)

  type (_, _, _) exists = Exists : ('a, 'm, 'b, 'x, 'n, 'y) t -> ('a, 'm, 'b) exists

  let exists : type a m b. (a, m, b) Dom.t -> (a, m, b) exists = function
    | Dim (x, a) -> Exists (Locks_dim (x, a))
    | Lock m -> Exists (Locks_lock m)
    | Proj _ -> raise (Failure "Locksmap.exists on Proj")

  let uniq : type a m b x1 x2 y1 y2 n1 n2.
      (a, m, b, x1, n1, y1) t -> (a, m, b, x2, n2, y2) t -> (x1 * n1 * y1, x2 * n2 * y2) Eq.t =
   fun f1 f2 ->
    match (f1, f2) with
    | Locks_dim (_, _), Locks_dim (_, _) -> Eq
    | Locks_lock _, Locks_lock _ -> Eq
end

module Locks = Path.Hom (TEntry) (Modality) (Locksmap)

(* Make a modality into a Tctx consisting only of locks. *)

module Lockmap = struct
  module Dom = Modality.Gen
  module Cod = Tctxcat

  module Obj = struct
    module Dom = Mode
    module Cod = ModeUnit

    type (_, _) t = Eq : 'a Mode.t -> ('a, 'a) t

    let dom : type a x. (a, x) t -> a Dom.t = fun (Eq a) -> a
    let cod : type a x. (a, x) t -> x Cod.t = fun (Eq a) -> Mode a

    type _ exists = Exists : ('a, 'x) t -> 'a exists

    let exists : type a. a Dom.t -> a exists = fun a -> Exists (Eq a)

    let uniq : type a x1 x2. (a, x1) t -> (a, x2) t -> (x1, x2) Eq.t =
     fun x1 x2 ->
      match (x1, x2) with
      | Eq _, Eq _ -> Eq
  end

  type (_, _, _, _, _, _) t =
    | Lock_lock :
        ('x, 'm, 'y) Modality.gen
        -> ('x, 'm, 'y, 'x, ('y Tctx.id, 'm lock_entry) snoc, 'y) t

  let dom : type a m b x n y. (a, m, b, x, n, y) t -> (a, m, b) Dom.t = function
    | Lock_lock m -> m

  let cod : type a m b x n y. (a, m, b, x, n, y) t -> (x, n, y) Cod.t = function
    | Lock_lock m -> Tctx.suc (Tctx.id (Mode (Modality.Gen.tgt m))) (Lock m)

  let src : type a m b x n y. (a, m, b, x, n, y) t -> (a, x) Obj.t = function
    | Lock_lock m -> Eq (Modality.Gen.src m)

  let tgt : type a m b x n y. (a, m, b, x, n, y) t -> (b, y) Obj.t = function
    | Lock_lock m -> Eq (Modality.Gen.tgt m)

  type (_, _, _) exists = Exists : ('a, 'm, 'b, 'x, 'n, 'y) t -> ('a, 'm, 'b) exists

  let exists : type a m b. (a, m, b) Dom.t -> (a, m, b) exists = fun m -> Exists (Lock_lock m)

  let uniq : type a m b x1 x2 y1 y2 n1 n2.
      (a, m, b, x1, n1, y1) t -> (a, m, b, x2, n2, y2) t -> (x1 * n1 * y1, x2 * n2 * y2) Eq.t =
   fun f1 f2 ->
    match (f1, f2) with
    | Lock_lock _, Lock_lock _ -> Eq
end

module Lock = Path.Hom (Modality.Gen) (Tctxcat) (Lockmap)
